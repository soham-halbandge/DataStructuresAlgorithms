package java_fundamentals.week1;

public class RelationalOperators {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 7;

        // relational operator give its result in boolean i.e. true or false
        System.out.println(num1 < num2); // checks whether num1 is less than num2
        System.out.println(num1 > num2); // checks whether num1 is greater than num2
        System.out.println(num1 <= num2); // checks whether num1 is less than or equal to num2
        System.out.println(num1 >= num2); // checks whether num1 is greater than or equal to num2
        System.out.println(num1 == num2); // checks whether num1 is equal to num2
        System.out.println(num1 != num2); // checks whether num1 is not equal to num2
    }
}
